library;

// ============================================================================
// 附件类型枚举
// ============================================================================

/// 附件选择类型
///
/// 用于附件选择器，定义用户可以选择的附件类型
enum ExtraItemType {
  /// 图片
  image,

  /// 文件
  file,

  /// 相机拍照
  camera,

  /// 语音录制
  voice,

  /// 视频录制
  video,

  /// 无附件
  none,
}

// ============================================================================
// UI 交互相关枚举
// ============================================================================

/// 发送按钮可见性模式
///
/// 用于控制输入框中发送按钮的显示行为
enum SendButtonVisibilityMode {
  /// 始终显示发送按钮（无论输入框是否为空）
  always,

  /// 仅在输入框有内容时显示发送按钮
  editing,

  /// 始终隐藏发送按钮
  hidden,
}
