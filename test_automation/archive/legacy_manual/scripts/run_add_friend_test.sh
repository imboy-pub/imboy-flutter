    echo -e "${CYAN}测试账号：${TEST_PHONE}${NC}"
    echo -e "${CYAN}日志文件：${LOG_FILE}${NC}"
    echo -e "${CYAN}========================================================================${NC}"
    echo ""
}

# 检查应用是否运行
check_app_running() {
    log_step "检查应用运行状态..."

    if pgrep -f "IMBoy.app/Contents/MacOS/IMBoy" > /dev/null; then
        log_success "IMBoy 应用正在运行"
        return 0
    else
        log_warning "IMBoy 应用未运行"
        log_info "正在启动应用..."
        open -a "${PROJECT_DIR}/${APP_PATH}"
        sleep 3
        if pgrep -f "IMBoy.app/Contents/MacOS/IMBoy" > /dev/null; then
            log_success "应用启动成功"
            return 0
        else
            log_error "应用启动失败"
            return 1
        fi
    fi
}

# 截图函数
take_screenshot() {
    local name=$1
    local filename="${SCREENSHOT_DIR}/${name}_${TIMESTAMP}.png"
    log_info "截图: ${filename}"
    # 使用 macOS 自带的截图命令
    screencapture -x "${filename}"
    sleep 0.5
}

